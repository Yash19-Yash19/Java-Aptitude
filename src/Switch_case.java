public class Switch_case {
    public static void main(String[] args) {
        int x=30;
        switch (x)
        {
            case 10:
                System.out.println("Value of x is 10");
                break;
            case 20:
                System.out.println("Value of x is 20");
                break;
            case 30:
                System.out.println("Value of x is 30");
                break;
            default:
                System.out.println("No match found");
        }
    }
}
